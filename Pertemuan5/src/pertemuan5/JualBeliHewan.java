private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("id_hewan");
        model.addColumn("nama_hewan");
        model.addColumn("jenis_hewan");
        model.addColumn("harga");
        
        try (
               Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
                Statement st = conn.createStatement(); 
                ResultSet rs = st.executeQuery("SELECT * FROM petville ORDER BY id_hewan")) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_hewan"),
                    rs.getString("nama_hewan"),
                    rs.getString("jenis_hewan"),
                    rs.getInt("harga"),
                   
                });
            }

            PETVILLE.setModel(model); // tampilkan ke JTable
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
}

