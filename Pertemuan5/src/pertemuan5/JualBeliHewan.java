    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        if (tfIDHewan.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua data");
        } else if (tfNamaHewan.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua data");
        } else if (tfJenisHewan.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua data");
        } else if (tfHarga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua data");
        } else {
            try {
                String id_hewan = tfIDHewan.getText();
                String nama_hewan = tfNamaHewan.getText();
                String jenis_hewan = tfJenisHewan.getText();
                int harga = Integer.parseInt(tfHarga.getText());

                connect();
                String sql = "INSERT INTO petville (id_hewan, nama_hewan, jenis_hewan, harga) VALUES (?, ?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, id_hewan);
                ps.setString(2, nama_hewan);
                ps.setString(3, jenis_hewan);
                ps.setInt(4, harga);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
                tampilData();
                bersih();
            } catch (NumberFormatException | SQLException e) {
                System.out.println("Gagal insert: " + e.getMessage());
            }

        }
    }//GEN-LAST:event_InsertActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        if (tfIDHewan.getText().equals("")
                || tfNamaHewan.getText().equals("")
                || tfJenisHewan.getText().equals("")
                || tfHarga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua data");
        } else {
            try {
            String sql = "UPDATE petville SET id_hewan = ?, nama_hewan = ?, jenis_hewan = ?, harga = ? WHERE id_hewan = ?";
            connect();
            PreparedStatement pst = conn.prepareStatement(sql);

            // Ambil dari textfield
            pst.setString(1, tfIDHewan.getText());
            pst.setString(2, tfNamaHewan.getText());
            pst.setString(3, tfJenisHewan.getText());
            pst.setInt(4, Integer.parseInt(tfHarga.getText()));
            pst.setString(5, tfIDHewan.getText()); // ID yang sudah auto increment

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

            // Refresh tabel
            tampilData();

            bersih();

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
    }//GEN-LAST:event_UpdateActionPerformed

